int		ft_atoi(char *str)
{
	int nb;
	int minus;

	minus = 1;
	nb = 0;
	while (*str == '\n' || *str == ' ' || *str == '\t' \
			|| *str == '\v' || *str == '\r' || *str == '\f')
	{
		str++;
	}
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			minus = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb *= 10;
		nb = nb + (*str - 48);
		str++;
	}
	nb *= minus;
	return (nb);
}
