#include "libft.h"

char	*strchr(const char *s, int c)
{
	const char	*str;

	str = (const char *)s;
	while (*s)
	{
		if (*s == c)
			return (s);
		s++;
	}
	if (*s == c)
		return (s);
	return (NULL);
}